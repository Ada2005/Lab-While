package Classes;
import java.util.*;
import java.io.*;

public class MySecondClass {
	
	public int add (int a, int b) {
		return a + b;
	}
	
	public int min (int a, int b) {
		return a - b;
	}
	
	public double div (int a, int b) {
		return a / b;
	}
	
	public int mul (int a, int b) {
		return a * b;
	}
	
	public int mod (int a, int b) {
		return a % b;
	}
		
}
